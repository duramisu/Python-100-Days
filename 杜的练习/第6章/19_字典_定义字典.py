# 定义有内容的字典
d1 = {'张三': 72, '李四': 60, '王五': 85}
print(d1)

# 字典中的key不能重复，若出现重复，则后写的会覆盖之前写的
d2 = {'张三': 72, '李四': 60, '王五': 85, '张三': 99}
print(d2)

# 定义空字典
d3 = {}
print(d3, type(d3))
d4 = dict()
print(d4, type(d4))


# 字典中的key必须是不可变类型，但value可以是任意类型
# 通俗理解：只有不可变的东西，才能作为key

# 错误示例：将列表作为key，是不行的
# d5 = {[1, 2, 3]: '张三'}
# print(d5)

# 字典可以嵌套
d6 = {'张三': {'年龄': 18, '成绩': 72}, '李四': {
    '年龄': 19, '成绩': 60}, '王五': {'年龄': 20, '成绩': 85}}
print(d6)
